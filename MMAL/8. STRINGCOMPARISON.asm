ORG 100H
.DATA
STRING1 DB 'DIU'
LEN EQU ($-STRING1)
STRING2 DB 'DIU'

OUTPUT DB 'NO$'
SUCCESSOUT DB 'YES$'


.CODE
LEA SI,STRING1
LEA DI,STRING2
MOV CX,LEN

REPE CMPSB
JNE NOT_EQUAL

LEA DX,SUCCESSOUT

JMP PRINT

NOT_EQUAL:
         LEA DX,OUTPUT

;PRINT

PRINT:
    MOV AH,09H
    INT 21H
    
RET   
